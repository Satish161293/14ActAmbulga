package castingStudy;

public class primitive_nonPrimitive {

	public static void main(String[] args) {
	// converting one type of data in to another type of data ( CASTING)
	// converting one data type of info in to another data type (PRIMITIVE CASTING) 
	// A) implicit   B) Explicit  C) boolean Casting
		
		int a= 100;
		System.out.println(a);
		float b=a;
		System.out.println(b);   // A) implicit
		
		System.out.println("=======SATISH======");
		
		float y= 140.456f;
		System.out.println(y);
		int x = (int) y;
		System.out.println(x);
		
		System.out.println("=======DESHMUKH======");
		
		

	}

}

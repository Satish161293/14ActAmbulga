package controlStetmentsStudy;

public class SwitchCaseStatementUse {

	public static void main(String[] args) {
		
		switch (5) {
		case 1:
			System.out.println("Satish");
			
			break;
		case 2:
			System.out.println("Deshmukh");
			break;
			
		case 3:
			System.out.println("Pooja");
           break;
		case 4:
			System.out.println("Patil");
           break;
		default:
			System.out.println("Plese enter the valid credential");
			break;
		}
	

	}

}
